/**
 * An enum containing all available modes for how modules in the plan view should be grouped. The following modes are
 * available:
 *
 *   - category: In this mode, modules are grouped according to their category property.
 *   - plan: In this mode, modules are grouped according to their plan they are part of.
 */
export enum PlanGroupingMode
{
    Category = "category",
    Plan = "plan",
}
